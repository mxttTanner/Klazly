#!/usr/bin/env node
// Concatenate all schema + migration SQL (in dependency order) into a single
// db/_bootstrap-all.sql, so a fresh NON-PROD Supabase can be brought up to the
// current schema by pasting one file into the SQL editor. Excludes the demo
// seed .sql (use `npm run db:seed` for demo data) and the optional
// worksheets-private flip. Re-run whenever migrations change.

import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const DB = join(dirname(fileURLToPath(import.meta.url)), "..", "db");

// Dependency-aware order. Base schema first, RLS-recursion fix + audit fixes
// last. Middle migrations are additive (add column/table/policy if not exists)
// so exact order among them is mostly forgiving.
const ORDER = [
  "schema.sql",
  "storage.sql",
  "worksheets.sql",
  "lesson-templates.sql",
  "messages.sql",
  "trial.sql",
  "subscription-plan.sql",
  "subscription-lifecycle.sql",
  "pause-and-cancel.sql",
  "pending-renewal.sql",
  "report-settings.sql",
  "users-phone.sql",
  "lessons-teacher-id-nullable.sql",
  "student-level.sql",
  "student-lesson-updates-unique.sql",
  "lesson-topic.sql",
  "lesson-unit.sql",
  "class-program.sql",
  "class-book.sql",
  "center-branding.sql",
  "center-notes.sql",
  "center-programs.sql",
  "attendance.sql",
  "feedback.sql",
  "founding-center.sql",
  "fix-rls-recursion.sql",
  "2026-07-02-audit-fixes.sql",
];

let out =
  "-- AUTO-GENERATED by scripts/concat-migrations.mjs — do not edit by hand.\n" +
  "-- Full schema + migrations for a fresh NON-PROD Supabase. Paste into the\n" +
  "-- SQL editor and run. Does NOT include demo data (npm run db:seed) or the\n" +
  "-- optional worksheets-private flip.\n\n";

for (const f of ORDER) {
  const sql = readFileSync(join(DB, f), "utf8");
  out += `\n-- ==========================================================================\n`;
  out += `-- ${f}\n`;
  out += `-- ==========================================================================\n`;
  out += sql.trimEnd() + "\n";
}

writeFileSync(join(DB, "_bootstrap-all.sql"), out, "utf8");
console.log(`Wrote db/_bootstrap-all.sql (${ORDER.length} files concatenated).`);
